class Calculator:
    """
    jednostavan kalkulator za osnovne arimeticke operacije
    """
    def __init__(self, a, b):
        """
        Args:
            a: prvi broj
            b: deugi broj

            returns:
                kreiran kalkulator
        """
    self.a = a
    self.b = b

def saberi(self):
    return self.a + self.b

def oduzmi(self):
    return self.a - self.b

def pomnozi(self):
    return self.a * self.b

def podijeli(self):
    return self.a / self.b